class Stack:
    #constructor de la pila
    def __init__(self):
        self.items=[]
    def insert(self, x):
        self.items.append(x)
    def pop_stack(self):
        #desapilar en la pila
        try:
            return self.items.pop()
        except IndexError:
            raise ValueError("La pila está vacía")
    def empty(self): #conocer su la pila esta vacia
        return self.items == []

